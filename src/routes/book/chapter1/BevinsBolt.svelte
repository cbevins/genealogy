<P>
    William Longford Bevins was born on 4 Mar 1815 in Newbold Verdon, Leicestershire, England.
</P>

<P>
    He married Ruth Gent on 13 Nov 1837 at Church Gresley, DerbyShire (about 4 miles southeast of Burton Upon Trent, Staffordshire). Ruth was born on 22 Jun 1817 in Donisthorpe, Derbyshire, England.  She gave birth to their son John Henry Bevins on 19 Mar 1838 at Ashby de la Zouchs,  Leicestershire.  But she apparently did not fare well during the process, dying shortly thereafter in April 1838 at the same location.  The 3 locales of Church Gresley, Donisthorpe, and Ashby de la Zouch form a triangle about 4 miles on a side, so they were probably residing in the Ashby de la Zouch vicinity at the time.
</P>

<P>
    Being a single father of an infant child, it was imperative that he quickly find another mate.  Two years later, he married ‘Mary Bolt’ (either in January or on March 15, 1840) in Market Bosworth, Leicestershire, England (English and Wales Civil Registration Index 1837-1900, Vol 15, page 209).  Her last name is variously given as Bolt, Bork, and Bott.  At age 17, she was 9 years younger than William.
</P>

<P>
    Mary Bolt was born in Barwell, Leicestershire, England on 19 Aug 1822, the ninth of ten children of Simon Bott and Mary Wykes.  Simon Bott died in 1854 in Market Bosworth, so her family may have been residing there around the time of her marriage to William.
</P>

<P>
    Mary died on 3 Jul 1878 in Annawan, Henry, Illinois (‘hemoplegia’?).  William Longford died 17 Jun 1889 in Annawan, Henry, Illinois.  They are both buried in Fairview Cemetery, adjacent to the Fairview United Methodist Chruch in Annawan.
</P>

<P>
    I have not explored Mary’s lineage much further at this point.  A good starting place is Ramonabevins72 ‘Bevins Family Tree’.  She names her mother as Mary Starbuck, born about 1783 in Thornton, Leicestershire, and father as Simon Bork, born 15 Sep 1779 in Bardon, Leicestershire.
</P>

